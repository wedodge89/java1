public class CharExamples {

    public static void main(String[] args) {
        char myChar = 'x';
        char myUnicode = '\u0041';
        char myUnicodeSupplement = '\u00AE';

        boolean isWeekday  = false;
        boolean isNight = true;

        System.out.println("Hello World!");
        System.out.println("myChar: " + myChar);
        System.out.println("myUnicode: " + myUnicode);
        System.out.println("myUnicodeSupplement: " + myUnicodeSupplement);
        System.out.println("isWeekday: " + isWeekday);
        System.out.println("isNight: " + isNight);
    }
}
